public class Recording
{
 
  String Title;
  String Artist;
  double seconds;
  
  public String getTitle()
  {
   return Title;
  }
  public void setTitle (String t)
  {
   Title = t;
  }
  
  public String getArtist()
  {
   return Artist;
  }  
   public void setArtist(String art)
  {
   Artist = art;
  }
  
   public double seconds()
  {
   return seconds;
  }
   public void setseconds(double s)
  { 
   seconds = s;
  }
  
 }


    
  
 
