public class CollegeCourse
{
  String courseID;
  int Credits;
  String letGrade; 

  public String getcourseID()
  {
   return courseID;
  }
  public void setcourseID(String i)
  {
   courseID = i;
  }
  public int getCredits()
  {
  return Credits;
  }
  public void setCredits(int c)
  {
   Credits = c;
  }
  public String getLetGrade()
  {
  return letGrade;
  }
  public void setLetGrade(String l)
  {
   letGrade = l;
  }
}
  